  /// Sorguya göre yerel listede arama yapar (büyük/küçük harf duyarsız,
  /// Türkçe karakter normalizasyonu ile).
  static List<FoodItem> search(String query) {
    if (query.trim().isEmpty) return [];
    final q = _normalize(query.toLowerCase().trim());
    return getAll().where((food) {
      return _normalize(food.name.toLowerCase()).contains(q);
    }).toList();
  }

  /// Türkçe karakterleri normalize eder (ı→i, ğ→g, ş→s, ü→u, ö→o, ç→c).
  static String _normalize(String text) {
    return text
        .replaceAll('ı', 'i')
        .replaceAll('ğ', 'g')
        .replaceAll('ş', 's')
        .replaceAll('ü', 'u')
        .replaceAll('ö', 'o')
        .replaceAll('ç', 'c')
        .replaceAll('İ', 'i')
        .replaceAll('Ğ', 'g')
        .replaceAll('Ş', 's')
        .replaceAll('Ü', 'u')
        .replaceAll('Ö', 'o')
        .replaceAll('Ç', 'c');
  }
